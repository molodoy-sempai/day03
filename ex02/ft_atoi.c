int	ft_atoi(char *s)
{
	int sign;
	int result;
	int i;

	i = 0;
	result = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n'
	|| s[i] == '\v' || s[i] == '\r' || s[i] == '\f' || s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (s[i] <= '9' && s[i] >= '0')
	{
		result = result * 10 + s[i] - '0';
		i++;
	}
	return (result * sign);
}
